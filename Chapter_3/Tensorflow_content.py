# TensorFlow advantages
# 1. 可自动计算任意可微表达式的梯度，这使其非常适合做机器学习。
# 2. 不仅可以在 CPU 上运行，还可以在 GPU、TPU 等高度并行的硬件加速器上运行。
#  定义的计算，很容易在多台机器上分布式进行。
# 3. 程序可以导出到其他运行时环境，
# 比如 C++、JavaScript（用于基于浏览器的应用程序）、TensorFlow Lite（用于移动设备或嵌入式设备上运行的应用程序）等。
# 这使得 TensorFlow 应用程序很容易部署到实际环境中。

# 组件包括用于强化学习研究的 TF-Agents，用于工业级机器学习工作流程管理的 TFX，
# 用于生产部署的 TensorFlow Serving，包含许多预训练模型的 TensorFlow Hub 代码库。

